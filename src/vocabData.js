// Get vocabulary pairs from a specific list
export const getVocabPairs = (listName) => {
    const dict = vocabularyLists[listName] || vocabDict;
    const pairs = Object.entries(dict).map(([italian, english]) => ({
        italian,
        english
    }));
    // Ensure Numbers list is in chronological order
    if (listName === 'Numbers') {
        const toNumber = (val) => {
            if (typeof val !== 'string') return Number.NaN;
            const cleaned = val.replace(/[.,\s€]/g, '');
            const n = parseInt(cleaned, 10);
            return Number.isNaN(n) ? Number.NaN : n;
        };
        pairs.sort((a, b) => {
            const na = toNumber(a.english);
            const nb = toNumber(b.english);
            if (Number.isNaN(na) || Number.isNaN(nb)) return 0;
            return na - nb;
        });
    }
    return pairs;
};

